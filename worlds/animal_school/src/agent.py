from unaiverse.agent import Agent


class AnimalSchoolRoles:
    # Role bitmasks
    ROLE_TEACHER = 1 << 2
    ROLE_STUDENT = 1 << 3

    # Feasible roles
    ROLE_BITS_TO_STR = {
        # The base roles will be inherited from AgentBasics later
        ROLE_TEACHER: "teacher",
        ROLE_STUDENT: "student",
    }


class WAgent(Agent, AnimalSchoolRoles):
    # feasible roles
    ROLE_BITS_TO_STR = {**Agent.ROLE_BITS_TO_STR, **AnimalSchoolRoles.ROLE_BITS_TO_STR}
    ROLE_STR_TO_BITS = {v: k for k, v in ROLE_BITS_TO_STR.items()}
